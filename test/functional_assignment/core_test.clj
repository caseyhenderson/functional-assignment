(ns functional-assignment.core-test
 (:require [clojure.test :refer :all]
  [functional-assignment.core :refer[morse-to-ascii ascii-to-morse remove-999 year-average month-lookup]]))

(deftest morse-to-ascii-test
  (testing "Morse to ASCII conversion, and ASCII to morse conversion"
    (is (= (morse-to-ascii ".... . .-.. .-.. ---  .-- --- .-. .-.. -..") "HELLO WORLD"))
    (is (=(ascii-to-morse "Hello World") ".... . .-.. .-.. ---  .-- --- .-. .-.. -.."))
    )
  )

(deftest helper-functions-test
  (def v [-999 5 7 10 -999]) 
  (testing "Removal of -999 values and month lookup"
    (is (= (remove-999 v) [5 7 10]))
    ; take one away to account for starting from zero
    (is (= (month-lookup (- 2 1)) "February"))))

